            p_matrix[i, :31] = 30.0 + (p_peak - 30.0) * (years[:31]/30.0)**0.45
            p_matrix[i, 31:] = 30.0 + (p_matrix[i, 30] - 30.0) * np.exp(-(years[31:]-30)/25.0)


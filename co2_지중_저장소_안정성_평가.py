    with torch.no_grad():
        for i in range(n_samples):
            phi_val, k_val = samples[0, i], samples[1, i]
            # PINN이 학습한 물성-압력 인과관계를 응답에 투영 (Surrogate Mapping)
            p_base_pred = model(x_fixed, y_fixed, z_fixed, torch.tensor([[10.0]]).to(device)).item()

